package com.damot;

public class Plane {
    private int Xdistance;
    private int Ydistance;

    public Plane(int Xdistance, int Ydistance) {
        this.Xdistance = Xdistance;
        this.Ydistance = Ydistance;
    }

    public int getXdistance() {
        return Xdistance;
    }

    public int getYdistance() {
        return Ydistance;
    }
}
